System.out.println(1 / 3);           // ==> 0   <== Uh oh, integer division 
System.out.println(1. / 3);          // ==> 0.333333
System.out.println(1.0 / 3);         // ==> 0.333333
System.out.println(1d / 3);          // ==> 0.333333
System.out.println(1D / 3);          // ==> 0.333333
System.out.println( (double)1 / 3 ); // ==> 0.3333333