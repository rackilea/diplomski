class Ideone
{
    static Scanner sc;

    public static void main (String[] args) throws java.lang.Exception
    {
        sc = new Scanner(System.in);
        int x = sc.nextInt();
        System.out.println(x);

        callit();
    }

    public static void callit()
    {
        int u = sc.nextInt();
        System.out.println(u);
    }
}