class Person {
    //These are its properties
    int height;
    int age;
    int weight;
    String name;

    //This is the constructor. You will call this to create an object and fill the properties.
    public Person(int height, int age, int weight, String name) {
        this.height = height;
        this.age = age;
        this.weight = weight;
        this.name = name;
    }
}