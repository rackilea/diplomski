class CardWithColAndRow {

    private int card;
    private int col;
    private int row;

    public CardWithColAndRow(int card, int col, int row) {
        this.card = card;
        this.col = col;
        this.row = row;
    }

    public int getCard() {
        return card;
    }

    public int getCol() {
        return col;
    }

    public int getRow() {
        return row;
    }
}