public class MyCircle {
    int radius;
    Color color;

    public MyCircle(int radius, Color color) {
        this.radius = radius;
        this.color = color;
    }

    //Getter Setter...
    public int getRadius() {
        return radius;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }

    public Color getColor() {
        return color;
    }

    public void setColor(Color color) {
        this.color = color;
    }
}