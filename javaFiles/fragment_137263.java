public class Movie {

    private String id;
    private String name;
    private String year;


    public String getId() {
        return id;
    }

    public void setReleaseYear(String year) {
        this.year = year;
    }

    public String getReleaseYear() {
        return year;
    }

    public String setMovieName(String name) {
        this.name = name;
    }

    public String getMovieName() {
        return name;
    }

    public String setMovieName(String name) {
        this.name = name;
    }
}