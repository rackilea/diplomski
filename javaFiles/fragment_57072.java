public class SettingsList {

    private int image;
    private String item;

    public SettingsList(int image, String item) {

        this.image = image;
        this.item = item;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getItem() {
        return item;
    }

    public void setItem(String item) {
        this.item = item;
    }
}