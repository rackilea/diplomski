public Dog(String name, String breed, char sex, int age, double weight){
    this("Chomp, chomp, chomp", "Woof, woof, woof");

    this.name = name;
    this.breed = breed;
    this.sex = sex;
    this.age = age;
    this.weight = weight;
}


public Dog(String eating, String barking){
    this.eating = eating;
    this.barking = barking;
}