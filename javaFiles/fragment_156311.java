public class ReturnDouble {

    static double returnF() {
        return 0F;
    }

    static double returnD() {
        return 0D;
    }

    static double returnI() {
        return 0;
    }

    static double returnL() {
        return 0L;
    }

    static double returnS() {
        return (short) 0;
    }

    static double returnC() {
        return '\0';
    }

}