// class to hold one set of values returned in the method above
public class TripleValue {
    private double a;
    private double b;
    private double c;

    public TripleValue(double a, double b, double c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }

    public double getC() {
        return c;
    }

}