class ExamplePojo {
    private int a1;
    private String b1;

    public ExamplePojo() {

    }

    public ExamplePojo(int a1, String b1) {
        this.a1 = a1;
        this.b1 = b1;
    }

    public int getA1() {
        return a1;
    }

    public void setA1(int a1) {
        this.a1 = a1;
    }

    public String getB1() {
        return b1;
    }

    public void setB1(String b1) {
        this.b1 = b1;
    }
}