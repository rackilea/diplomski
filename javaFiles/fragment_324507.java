public class Device {
        String name;
        String rssi;
        String uuid;
        public String getName() {
                return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public String getRssi() {
            return rssi;
        }

        public void setRssi(String rssi) {
            this.rssi = rssi;
        }

        public String getUuid() {
            return uuid;
        }

        public void setUuid(String uuid) {
            this.uuid = uuid;
        }


}