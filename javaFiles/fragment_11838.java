public class HourData {

   Integer imp;
   Integer clk;
   String spend;
   Integer conv;
   String cpm;
   String cpc;
   String cpa;

   @Override
   public String toString() {
      return "HourData [imp=" + imp + ", clk=" + clk + ", spend=" + spend
            + ", conv=" + conv + ", cpm=" + cpm + ", cpc=" + cpc + ", cpa="
            + cpa + "]";
   }

 }