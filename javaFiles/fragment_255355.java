public class MyPojo
{
    /*
     * Private class variables
     */
    private String name;
    private int size;

    /**
     * Empty constructor
     */
    public MyPojo()
    {
    }

    /*
     * Standard getter/setters 
     */
    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public int getSize()
    {
        return size;
    }

    public void setSize(int size)
    {
        this.size = size;
    }   
}