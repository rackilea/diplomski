char color;
short number;

public Card(char color, short number) {
    this.color = color;
    this.number = number;

}

public char getColor() {
    return color;
}

public void setColor(char color) {
    this.color = color;
}

public short getNumber() {
    return number;
}

public void setNumber(short number) {
    this.number = number;
}