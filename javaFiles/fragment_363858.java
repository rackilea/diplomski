public class GridReference {

    private final int _x;

    private final int _y;

    private final int _z;

    public GridReference(int x, int y, int z) {
        _x = x;
        _y = y;
        _z = z;
    }

    public getX() {
        return _x;
    }

    public getY() {
        return _y;
    }

    public getZ() {
        return _z;
    }

}