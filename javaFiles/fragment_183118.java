class BillBundle
{
    public final int fives;
    public final int tens;
    public final int twenties;
    public final int fifties;

    public BillBundle(int fives, int tens, int twenties, int fifties)
    {
       this.fives = fives;
       this.tens = tens;
       this.twenties = twenties;
       this.fifties = fifties;
    }
}